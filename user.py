# Програма, що запитує у користувача його ім'я, вік та місце проживання.
# Після цього виводить наступні три рядки:
# "This is <ім'я>"
# "It is <вік>"
# "S(he) lives in <місце проживання>"

name = input("What is your name? ")
age = input("How old are you? ")
residence = input("Where do you live? ")

print(f"This is {name}")
print(f"It is {age}")
print(f"(S)he lives in {residence}")
